package calculadora;

public class Divisao {  /* clsse pública "Divisao", pertencente ao pacote "calculadora" */
    
   public int dividir(int a, int b)  {  /* método público "dividir" da classe Divisao com retorno do tipo inteiro 
                                        que recebe como parametro dois números também inteiros ("a" e "b") */
       
       return a / b;  /* o retorno do método é igual ao valor recebido em "a" dividido pelo valor recebido em "b" */
   }
}
