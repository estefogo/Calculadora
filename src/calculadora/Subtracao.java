package calculadora;

public class Subtracao {  /* clsse pública "Subtracao", pertencente ao pacote "calculadora" */
 
    public int subtrair(int a, int b) {  /* método público "subtrair" da classe Subtracao com retorno do tipo inteiro 
                                      que recebe como parametro dois números também inteiros ("a" e "b") */
        
        return a - b;  /* o retorno do método é igual ao valor recebido em "b" subtraído do valor recebido em "a" */
    }
}
