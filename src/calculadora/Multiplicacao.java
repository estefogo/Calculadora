package calculadora;

public class Multiplicacao {  /* clsse pública "Multiplicacao", pertencente ao pacote "calculadora" */

    public int multiplicar(int a, int b) {  /* método público "multipicar" da classe Multiplicacao com retorno do tipo inteiro 
                                            que recebe como parametro dois números também inteiros ("a" e "b") */
        
        return a * b;  /* o retorno do método é igual ao valor recebido em "a" multiplicado pelo valor recebido em "b" */
    }
}
