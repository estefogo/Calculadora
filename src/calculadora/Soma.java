package calculadora;

public class Soma {  /* clsse pública "Soma", pertencente ao pacote "calculadora" */
    
    public int somar(int a, int b) {  /* método público "somar" da classe Soma com retorno do tipo inteiro 
                                      que recebe como parametro dois números também inteiros ("a" e "b") */
        
        return a + b;  /* o retorno do método é igual ao valor recebido em "a" somado ao valor recebido em "b" */
    }

}